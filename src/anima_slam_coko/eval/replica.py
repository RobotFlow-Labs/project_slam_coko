"""Replica dataset evaluation runner (Paper Table 1).

Computes per-scene rendering metrics (PSNR, SSIM, depth-L1) for all
training-view keyframes and generates a gap report against paper targets.
"""

from __future__ import annotations

import json
from dataclasses import asdict, dataclass
from pathlib import Path

import numpy as np

from anima_slam_coko.eval.metrics import (
    PAPER_TARGETS,
    compare_to_paper,
    depth_l1,
    psnr,
    ssim,
)


@dataclass(slots=True)
class SceneResult:
    scene: str
    psnr_mean: float
    ssim_mean: float
    depth_l1_mean: float
    num_frames: int
    paper_gaps: dict[str, float]


def evaluate_scene(
    scene: str,
    pred_images: list[np.ndarray],
    gt_images: list[np.ndarray],
    pred_depths: list[np.ndarray] | None = None,
    gt_depths: list[np.ndarray] | None = None,
) -> SceneResult:
    """Evaluate a single Replica scene.

    Args:
        scene: scene name (e.g. ``"office_0"``).
        pred_images: list of rendered ``(H, W, 3)`` float images in ``[0, 1]``.
        gt_images: list of ground-truth ``(H, W, 3)`` float images in ``[0, 1]``.
        pred_depths: optional list of ``(H, W)`` predicted depth maps.
        gt_depths: optional list of ``(H, W)`` GT depth maps.

    Returns:
        :class:`SceneResult` with per-scene averages and paper gaps.
    """
    psnr_vals = [psnr(p, g) for p, g in zip(pred_images, gt_images)]
    ssim_vals = [ssim(p, g) for p, g in zip(pred_images, gt_images)]

    dl1_vals = []
    if pred_depths and gt_depths:
        dl1_vals = [
            depth_l1(pd, gd) for pd, gd in zip(pred_depths, gt_depths)
        ]

    metrics = {
        "psnr": float(np.mean(psnr_vals)) if psnr_vals else 0.0,
        "ssim": float(np.mean(ssim_vals)) if ssim_vals else 0.0,
        "depth_l1": float(np.mean(dl1_vals)) if dl1_vals else 0.0,
    }

    scene_key = f"replica_{scene}"
    gaps = compare_to_paper(scene_key, metrics)

    return SceneResult(
        scene=scene,
        psnr_mean=metrics["psnr"],
        ssim_mean=metrics["ssim"],
        depth_l1_mean=metrics["depth_l1"],
        num_frames=len(pred_images),
        paper_gaps=gaps,
    )


def write_gap_report(
    results: list[SceneResult], output_path: Path
) -> None:
    """Write a markdown gap report comparing results to paper targets."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# SLAM-COKO Paper Gap Report — Replica",
        "",
        "| Scene | PSNR | Paper | Gap | SSIM | Depth-L1 |",
        "|-------|------|-------|-----|------|----------|",
    ]
    for r in results:
        target = PAPER_TARGETS.get(f"replica_{r.scene}", {})
        paper_psnr = target.get("psnr", "N/A")
        gap = r.paper_gaps.get("psnr", "N/A")
        if isinstance(gap, float):
            gap = f"{gap:+.3f}"
        lines.append(
            f"| {r.scene} | {r.psnr_mean:.3f} | {paper_psnr} | {gap} | "
            f"{r.ssim_mean:.4f} | {r.depth_l1_mean:.4f} |"
        )
    lines.extend(["", f"Generated by SLAM-COKO evaluation pipeline."])

    output_path.write_text("\n".join(lines) + "\n")

    # Also write machine-readable JSON
    json_path = output_path.with_suffix(".json")
    json_path.write_text(json.dumps([asdict(r) for r in results], indent=2))
