"""Aria dataset evaluation runner (Paper Table 2).

Computes per-room novel-view rendering metrics and generates a gap report.
"""

from __future__ import annotations

import json
from dataclasses import asdict, dataclass
from pathlib import Path

import numpy as np

from anima_slam_coko.eval.metrics import (
    PAPER_TARGETS,
    compare_to_paper,
    depth_l1,
    psnr,
    ssim,
)


@dataclass(slots=True)
class RoomResult:
    room: str
    psnr_mean: float
    ssim_mean: float
    depth_l1_mean: float
    num_frames: int
    paper_gaps: dict[str, float]


def evaluate_room(
    room: str,
    pred_images: list[np.ndarray],
    gt_images: list[np.ndarray],
    pred_depths: list[np.ndarray] | None = None,
    gt_depths: list[np.ndarray] | None = None,
) -> RoomResult:
    """Evaluate a single Aria room."""
    psnr_vals = [psnr(p, g) for p, g in zip(pred_images, gt_images)]
    ssim_vals = [ssim(p, g) for p, g in zip(pred_images, gt_images)]

    dl1_vals = []
    if pred_depths and gt_depths:
        dl1_vals = [
            depth_l1(pd, gd) for pd, gd in zip(pred_depths, gt_depths)
        ]

    metrics = {
        "psnr": float(np.mean(psnr_vals)) if psnr_vals else 0.0,
        "ssim": float(np.mean(ssim_vals)) if ssim_vals else 0.0,
        "depth_l1": float(np.mean(dl1_vals)) if dl1_vals else 0.0,
    }

    room_key = f"aria_{room}"
    gaps = compare_to_paper(room_key, metrics)

    return RoomResult(
        room=room,
        psnr_mean=metrics["psnr"],
        ssim_mean=metrics["ssim"],
        depth_l1_mean=metrics["depth_l1"],
        num_frames=len(pred_images),
        paper_gaps=gaps,
    )


def write_gap_report(
    results: list[RoomResult], output_path: Path
) -> None:
    """Write a markdown gap report comparing results to paper targets."""
    output_path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "# SLAM-COKO Paper Gap Report — Aria",
        "",
        "| Room | PSNR | Paper | Gap | SSIM | Depth-L1 |",
        "|------|------|-------|-----|------|----------|",
    ]
    for r in results:
        target = PAPER_TARGETS.get(f"aria_{r.room}", {})
        paper_psnr = target.get("psnr", "N/A")
        gap = r.paper_gaps.get("psnr", "N/A")
        if isinstance(gap, float):
            gap = f"{gap:+.3f}"
        lines.append(
            f"| {r.room} | {r.psnr_mean:.3f} | {paper_psnr} | {gap} | "
            f"{r.ssim_mean:.4f} | {r.depth_l1_mean:.4f} |"
        )
    lines.extend(["", "Generated by SLAM-COKO evaluation pipeline."])

    output_path.write_text("\n".join(lines) + "\n")
    json_path = output_path.with_suffix(".json")
    json_path.write_text(json.dumps([asdict(r) for r in results], indent=2))
